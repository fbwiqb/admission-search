changes_log = []

def update_entry(sheet_name, match_criteria, updates):
    sheet = data.get(sheet_name, [])
    for i, entry in enumerate(sheet):
        matched = all(entry.get(k) == v for k, v in match_criteria.items())
        if matched:
            for field, new_value in updates.items():
                old_value = entry.get(field, "")
                if str(old_value) != str(new_value):
                    entry[field] = new_value
                    changes_log.append({
                        "sheet": sheet_name,
                        "idx": i,
                        "univ": entry.get("대학명", ""),
                        "field": field,
                        "old": old_value,
                        "new": new_value
                    })
            return True
    print(f"  NOT FOUND: {sheet_name} / {match_criteria}")
    return False

def count_gaps(sheet_data, skip_fields=None):
    if skip_fields is None:
        skip_fields = {"출처"}
    total = 0
    empty = 0
    for entry in sheet_data:
        for k, v in entry.items():
            if k in skip_fields:
                continue
            total += 1
            if v == "" or v is None:
                empty += 1
    return empty, total

