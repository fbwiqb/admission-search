def make(univ, dept, typ, name, people, choi, choi_detail, interview, interview_type,
         satam_susi="", satam_jeongsi="", change="", **extra):
    row = {
        "대학명": univ, "학과": dept, "전형유형": typ, "전형명": name,
        "모집인원": people, "최저유무": choi, "최저내용": choi_detail,
        "면접유형": interview_type,
        "사탐허용(수시)": satam_susi, "사탐허용(정시)": satam_jeongsi,
        "2027변경사항": change, "출처": "웹(4중검증)",
        **common, "전형구분": typ, "면접유무": interview, "2차전형": "",
    }
    if interview == "O":
        row["2차전형"] = interview_type if interview_type else "면접"
    else:
        row["2차전형"] = "없음"
    row.update(extra)
    return row

